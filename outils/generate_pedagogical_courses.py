def render_module(
    item: dict[str, Any],
    book: str,
    domain: str,
    index: int,
    total_modules: int,
    lesson_count: int,
    notes: list[str],
) -> str:
    start, end = lesson_span(index, total_modules, lesson_count)
    concept_lines = "\n".join(f"- {concept}" for concept in item["concepts"])
    table_rows = "\n".join(
        f"| `{example}` | {label} | {benefit} |" for example, label, benefit in item["examples"]
    )
    solved = "\n".join(
        f"- `{example}`: {benefit}" for example, _label, benefit in item["examples"]
    )
    questions = "\n".join(
        f"{number}. {question}\n   - الجواب: {answer}"
        for number, (question, answer) in enumerate(question_set(book, domain, item["title"], item["examples"]), start=1)
    )
    return f"""## {item["title"]}

### الهدف

{item["goal"]}

### موضعه من السماع

يراجع هذا الباب مع الأصوات القريبة من {start} إلى {end}. لا يعني ذلك أن كل مسألة محصورة في هذه الأصوات، لكنه موضع مناسب للربط بين التفريغ الصوتي والمتن والشرح.

### خلاصة مركزة

{concept_lines}

### جدول تطبيق

| الباب | المثال | كيف يستفيد الطالب |
|---|---|---|
{table_rows}

### أمثلة محلولة

{solved}

### مراجعة من المتن والشرح

{source_block(notes)}

### تدريب

{item["drill"]}

### أسئلة التحقق

{questions}
"""


def render_course(identifier: str) -> str:
    profile = resolved_profile(identifier)
    book = profile["book"]
    domain = profile["domain"]
    modules = profile["modules"]
    inventory = read_inventory(identifier)
    notes = source_notes(identifier)
    rendered_modules = [
        render_module(item, book, domain, index, len(modules), len(inventory), notes)
        for index, item in enumerate(modules, start=1)
    ]
    return f"""# {book}

{profile["intro"]}

هذا الملف مبني ليكون مثل مسار الآجرومية والمتممة: وحدات مفهومة، أمثلة، جدول، تدريب، وأسئلة تحقق. التفريغ الصوتي يبقى مرجعًا في تبويب `Transcription`، أما هنا فالمادة مرتبة للتعلم والمراجعة.

## طريقة الدراسة

- اقرأ الهدف قبل سماع المقطع.
- راجع الجدول ثم الأمثلة المحلولة.
- افتح المتن والشرح من تبويب `Matn / Charh`.
- أجب عن الأسئلة دون النظر إلى الجواب أولًا.
- ارجع إلى التفريغ الصوتي عند المسألة التي تحتاج سماعًا أطول.

""" + "\n\n".join(rendered_modules)


def generate_course(identifier: str, overwrite: bool = False) -> bool:
    if identifier in PROTECTED_COURSES:
        return False
    if identifier not in COURSE_PROFILES:
        return False
    out = PEDAGOGICAL_DIR / identifier / "cours.md"
    if out.exists() and not overwrite:
        return False
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(render_course(identifier), encoding="utf-8")
    return True


def main() -> None:
    changed = []
    for item_dir in sorted(ARCHIVE_DIR.iterdir()):
        if not (item_dir / "inventory.tsv").exists():
            continue
        if generate_course(item_dir.name, overwrite=True):
            changed.append(item_dir.name)
    print(f"generated={len(changed)}")
    for identifier in changed:
        print(identifier)


if __name__ == "__main__":
    main()
