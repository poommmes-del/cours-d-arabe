def source_notes(identifier: str) -> list[str]:
    text = read(SOURCES_MD)
    match = re.search(rf"^###\s+{re.escape(identifier)}\s*$", text, flags=re.MULTILINE)
    if not match:
        return []
    start = match.end()
    next_match = re.search(r"^###\s+", text[start:], flags=re.MULTILINE)
    section = text[start : start + next_match.start()] if next_match else text[start:]
    notes = []
    for line in section.splitlines():
        if any(marker in line for marker in ("Matn:", "Sharh", "شرح", "Livre du cours", "Reference")):
            notes.append(line.strip("- ").strip())
    return notes[:4]


def resolved_profile(identifier: str) -> dict[str, Any]:
    profile = COURSE_PROFILES[identifier]
    if "same_as" not in profile:
        return profile
    base = COURSE_PROFILES[profile["same_as"]]
    replacements = profile.get("title_replacements", {})
    modules = []
    for item in base["modules"]:
        copied = dict(item)
        title = copied["title"]
        for old, new in replacements.items():
            title = title.replace(old, new)
        copied["title"] = title
        modules.append(copied)
    return {
        "book": profile["book"],
        "domain": profile["domain"],
        "intro": profile["intro"],
        "modules": modules,
    }


def lesson_span(index: int, total_modules: int, lesson_count: int) -> tuple[int, int]:
    start = round((index - 1) * lesson_count / total_modules) + 1
    end = round(index * lesson_count / total_modules)
    return max(1, start), max(start, min(lesson_count, end))


